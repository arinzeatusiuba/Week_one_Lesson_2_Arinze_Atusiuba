const dayOfWeek = 'monday';

switch (dayOfWeek) {
  case 'sunday' :
    console.log('Sunday: The first day of the week');
    break;
  case 'monday' :
    console.log('Monday: Start of the week');
    break;
  case 'tuesday' :
    console.log('Tuesday: The third day of the week');
    break;
  case 'wednesday' :
    console.log('Wednesday: The fourth day of the week');
    break;
  case 'thursday' :
    console.log('Thursday: The fifth day of the week');
    break;
  case 'friday' :
    console.log('Friday: The sixth day of the week');
    break;
  case 'saturday' :
    console.log('Saturday: The seventh day of the week');
    break;
  default :
    console.log('Not day of the week');
    break;
}
